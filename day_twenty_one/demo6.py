piano_keys = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

print(piano_keys[2:5])
print(piano_keys[:5])
print(piano_keys[2:5:2])
print(piano_keys[::2]) # skipping every single item
print(piano_keys[::-1]) # reversing the list


piano_tuple = ("do", "re", "mi", "fa", "so", "la", "ti")
print(piano_tuple[:5]) # also works with tuple